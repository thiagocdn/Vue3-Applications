<template>
  <div class="m-auto">
    <h1 class="text-2xl text-center my-2">Vue Calendar</h1>
    <section class="mx-2 flex justify-between">
      <h2 class="font-bold">{{currentMonthName}}</h2>
      <h2 class="font-bold">{{currentYear}}</h2>
    </section>
    <section class="flex">
      <p class="text-center my-2" style="width: 14.28%" v-for="day in days" :key="day">{{ day }}</p>
    </section>
    <section class="flex flex-wrap">
      <p class="text-center" style="width: 14.28%" v-for="num in startDay()" :key="num"></p>
      <p class="text-center" style="width: 14.28%" v-for="num in daysInMonth(2021, 1)" :key="num">{{num}}</p>
    </section>
  </div>
</template>

<script>
export default {
  data() {
    return {
      currentMonth: new Date().getMonth() + 1,
      currentMonthName: new Date().toLocaleString("default", { month: 'long' }),
      currentYear: new Date().getFullYear(),
      days: ['Sun', 'Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat']
    }
  },
  methods: {
    daysInMonth(year, month) {
      return new Date(year, month, 0).getDate();
    },
    startDay() {
      return new Date(this.currentYear, this.currentMonth - 1, 1).getDay();
    }
  }
}
</script>

